(function(Rhombus) {
  Rhombus._patternSetup = function(r) {

    var patternId = 0;

    r.Pattern = function() {
      this._noteMap = {};
      this._id = patternId;
      patternId = patternId + 1;
    };

    r.Note = function(pitch, start, length, id) {
      if (id) {
        r._setId(this, id);
      } else {
        r._newId(this);
      }
      this._pitch = pitch || 60;
      this._start = start || 0;
      this._length = length || 0;
    };

    r.Note.prototype = {
      getPitch: function() {
        return this._pitch;
      },

      getStart: function() {
        return this._start;
      },

      getLength: function() {
        return this._length;
      },

      getEnd: function() {
        return this._start + this._length;
      }
    };
  };
})(this.Rhombus);
